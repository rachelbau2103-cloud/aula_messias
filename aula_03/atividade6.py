# Atividade 06 - Nome, idade e cálculo de idade em 2025

# Entrada de dados
nome = input("Digite seu nome: ")
Idade = int(input("Digite sua idade: "))

# Calculo idade em 2025
ano_atual = 2025
Idade_2025 = Idade + (2025 - ano_atual)

# Saída
print(f"Nome: {nome}, em 2025 você terá {Idade_2025}anos.")

